package oit.is.jinro.jinrogame.model;

public class Role {
  String roleName;
  String camp;

  public Role() {

  }

  public String getroleName() {
    return roleName;
  }

  public void setroleName(String roleName) {
    this.roleName = roleName;
  }

  public String getcamp() {
    return camp;
  }

  public void setcamp(String camp) {
    this.camp = camp;
  }
}
